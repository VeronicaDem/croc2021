package ru.croc.java.transport.enums;

/**
 * Тип транспорта. Разделение по воздушным и наземным видам
 */
public enum TransportType {
    /**
     * Воздушный транспорт (самолеты, вертолеты и т.д.)
     */
    AERO,
    /**
     * Наземный транспорт (железная дорога, трамвай и т.д.)
     */
    TERRESTRIAL
}
