package ru.croc.java.transport.enums;

/**
 * Тип транспорта, разделение по использованию: общего пользования, частного и специальный транспорт.
 */
public enum UseType {
    /**
     * Общего пользования.
     */
    PUBLIC,
    /**
     * Специального использования.
     */
    SPECIFICAL,
    /**
     * Личного пользования.
     */
    INDIVIDUAL
}
